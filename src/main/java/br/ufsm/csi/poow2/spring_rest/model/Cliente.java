package br.ufsm.csi.poow2.spring_rest.model;

public class Cliente {
    private int id;
    private String nome;

    private Cliente(){}

    public Cliente(int id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
